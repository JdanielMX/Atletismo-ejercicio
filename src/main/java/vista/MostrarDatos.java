    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if (tblAtletas.getRowCount() > 0) {

            if (tblAtletas.getSelectedRow() != -1) {

                int opcion = JOptionPane.showConfirmDialog(null, "¬øEstas seguro de eliminar el elemento seleccionado?");

                if (opcion == 0) {

                    Long indice = Long.parseLong(String.valueOf(tblAtletas.getValueAt(tblAtletas.getSelectedRow(), 0)));
                    repos.eliminar(indice);
                    JOptionPane.showMessageDialog(null, "Datos eliminados");
                    cargarTabla();
                }

            } else {

                JOptionPane.showMessageDialog(null, "No has seleccionado el valor a eliminar");

            }

        } else {

            JOptionPane.showMessageDialog(null, "No hay Datos en la tabla");

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (tblAtletas.getRowCount() > 0) {

            if (tblAtletas.getSelectedRow() != -1) {

                Long indice = Long.parseLong(String.valueOf(tblAtletas.getValueAt(tblAtletas.getSelectedRow(), 0)));

                ActualizaDatos actualizaDatos = new ActualizaDatos(indice);
                actualizaDatos.setVisible(true);
                actualizaDatos.setLocationRelativeTo(null);
                this.dispose();

            } else {

                JOptionPane.showMessageDialog(null, "No has seleccionado el valor a actualizar");

            }

        } else {

            JOptionPane.showMessageDialog(null, "No hay Datos en la tabla");

        }


    }//GEN-LAST:event_btnEditarActionPerformed

