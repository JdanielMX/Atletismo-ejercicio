    private void cargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int r, int c) {
                return false;
            }
        };

        String[] titulos = {"ID", "NOMBRE", "NUMERO", "TIEMPO"};

        tabla.setColumnIdentifiers(titulos);

        List<Atleta> lista = new ArrayList();

        lista = repos.listar();

        if (!lista.isEmpty()) {

            for (Atleta at : lista) {

                Object[] datos = {at.getId(), at.getNombre(), at.getNumeroAtleta(), at.getTiempo()};

                tabla.addRow(datos);

            }

        } else {

            btnEditar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnVerDatos.setEnabled(false);

            JOptionPane.showMessageDialog(null, "A un no hay atletas registrados");
        }

        tblAtletas.setModel(tabla);

    }
